        // POST: /Account/Register
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> Register(RegisterViewModel model)
        {
            if (ModelState.IsValid)
            {
                var school = db.Schools.ToList().Find(p=>p.name == model.schoolname);
                if (school == null)
                {
                    ModelState.AddModelError("", model.schoolname + " is not registered with BlckBook yet");
                    return View(model);
                }
                byte[] imageData = new byte[0];
                if (model.poImgFile != null && model.poImgFile.ContentLength > 0)
                {
                    using (var binary = new BinaryReader(model.poImgFile.InputStream))
                    {
                        imageData = binary.ReadBytes(model.poImgFile.ContentLength);
                    }
                }
                var user = new ApplicationUser { UserName = model.Email, Email = model.Email, userPhoto = imageData ,firstName = model.firstName, middleName = model.middleName, lastName = model.lastName};
                var result = await UserManager.CreateAsync(user, model.Password);
                if (result.Succeeded)
                {
                    if(model.role == "Student")
                    {
                        var student = new Student { Id = user.Id, dob = DateTime.Today, _date = DateTime.Now, school = school };
                        db.Students.Add(student);
                        db.SaveChanges();
                        await this.UserManager.AddToRoleAsync(user.Id, "GuestS");
                    }
                    if (model.role == "Teacher")
                    {
                        var teacher = new Teacher { Id = user.Id, dob = DateTime.Today, _date = DateTime.Now, school = school };
                        db.Teachers.Add(teacher);
                        db.SaveChanges();
                        await this.UserManager.AddToRoleAsync(user.Id, "GuestT");
                    }
                    await SignInManager.SignInAsync(user, isPersistent:false, rememberBrowser:false);

                    // For more information on how to enable account confirmation and password reset please visit http://go.microsoft.com/fwlink/?LinkID=320771
                    // Send an email with this link
                    string code = await UserManager.GenerateEmailConfirmationTokenAsync(user.Id);
                    var callbackUrl = Url.Action("ConfirmEmail", "Account", new { userId = user.Id, code = code }, protocol: Request.Url.Scheme);
                    await UserManager.SendEmailAsync(user.Id, "Confirm your account", "Please confirm your account by clicking <a href=\"" + callbackUrl + "\">here</a>");

                    var userManager = new UserManager<ApplicationUser>(new UserStore<ApplicationUser>(db));
                    var s = userManager.GetRoles(user.Id);
                    
                    if (s[0].ToString() == "GuestS")
                    {
                        return RedirectToAction("Student", "Classes");
                    }
                    if (s[0].ToString() == "GuestT")
                    {
                        return RedirectToAction("Teacher", "Classes");
                    }
                }
                AddErrors(result);
            }
            // If we got this far, something failed, redisplay form
            return View(model);
        }

