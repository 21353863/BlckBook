        [HttpPost]
        public ActionResult ChangeDp(ChangeDpViewModel changeDpViewModel)
        {
            string id = User.Identity.GetUserId();
            var user = db.Users.Find(id);
            if (ModelState.IsValid)
            {
                byte[] imageData = new byte[0];
                if (changeDpViewModel.poImgFile != null && changeDpViewModel.poImgFile.ContentLength > 0)
                {
                    using (var binary = new BinaryReader(changeDpViewModel.poImgFile.InputStream))
                    {
                        imageData = binary.ReadBytes(changeDpViewModel.poImgFile.ContentLength);
                    }
                }
                user.userPhoto = imageData;
                db.Entry(user).State = EntityState.Modified;
                db.SaveChanges();
                return RedirectToAction("ChangeDp","Account");
            }
            return View(changeDpViewModel);
        }

